package cn.cyberict.ncha.business.commons.DocumentUtils;


public class MultipleImage {


    
    private float x;
    
    private float y;

    public MultipleImage(float imageWidth, float imageHeight) {
        this.x = imageWidth;
        this.y = imageHeight;
    }

    public float getX() {
        return x;
    }

    public void setX(float x) {
        this.x = x;
    }

    public float getY() {
        return y;
    }

    public void setY(float y) {
        this.y = y;
    }


}
